#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	i;
	size_t			len;
	char			*str;

	len = ft_strlen(s1);
	str = (char *)ft_memalloc(sizeof(*str) * len + 1);
	if (str == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s1[i];
		i++;
	}
	return (str);
}
